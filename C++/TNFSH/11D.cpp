#include<iostream>
using namespace std;

int main(){//高雄發大財
    int n, x, num;//n議員總數; x抽籤次數; num數字
    int c=0;//c判斷
    int i, j;//i, j旗標

    cin>>n;

    for(i=1; i<=n; ++i){
        cin>>x;

        for(j=0; j<x; ++j){
            cin>>num;

            if(num==87)
                c=1;
        }
        cout<<i<<':';

        if(c==0)
            cout<<"No\n";
        else
            cout<<"YES\n";//判斷、輸出結果
        c=0;//初始化
    }
    return 0;
}
